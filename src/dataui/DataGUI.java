    private void startupConfig() {
        try{
            if(Files.notExists(DIRECTORY_PATH)){
                Files.createDirectories(DIRECTORY_PATH);
                System.err.println("Directory created");
            }
            if(Files.notExists(USERS_FILE_PATH)){
                Files.createFile(USERS_FILE_PATH);
                System.err.println("Users file created");
            }
            if(Files.notExists(RATINGS_FILE_PATH)){
                Files.createFile(RATINGS_FILE_PATH);
                System.err.println("Ratings file created");
            }
        } catch(IOException e){
            System.err.println(e.getMessage());
        } catch(Exception e){
            System.err.println(e.getMessage());
        }
    }

    private boolean updateUserFile() {
        Vector<Vector> datas = user_model.getDataVector();
        String text = "";
        if(!datas.isEmpty()){
            for(var data: datas){
                Object[] d = data.toArray();
                for(int i = 0; i < data.size(); i++){
                    text += d[i] + ",";
                }
                text += System.lineSeparator();
            }
            try{
                Files.writeString(USERS_FILE_PATH, text, StandardOpenOption.APPEND);
                return true;
            } catch(IOException e){
                JOptionPane.showConfirmDialog(rootPane, "Something went wrong while writing User file!!", "Error", JOptionPane.OK_CANCEL_OPTION, JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        return true;
    }
    
