    private void updateOverallRating(){
        int slider1_val = jSlider1.getValue();
        int slider2_val = jSlider2.getValue();
        int slider3_val = jSlider3.getValue();
        int slider4_val = jSlider4.getValue();
        int slider_sum = slider1_val + slider2_val + slider3_val + slider4_val;
        int overall_rating = Math.round(slider_sum / 4);
        jLabel16.setText(String.valueOf(overall_rating));
    }
    private void getAllUsers(){
        int rows = jTable1.getRowCount();
        user_combomodel.removeAllElements();
        user_combomodel.addElement("Select an user");
        user_combomodel.setSelectedItem("Select an user");
        if(rows == 0){
            JOptionPane.showConfirmDialog(rootPane, "Create a user first!", "Error", JOptionPane.OK_CANCEL_OPTION ,JOptionPane.ERROR_MESSAGE);
        }
        else{
            for(int i = 0; i < rows; i++){
                String userId = jTable1.getValueAt(i, 0).toString();
                String userName = jTable1.getValueAt(i, 1).toString();
                String user = userName + ":" + userId;
                user_combomodel.addElement(user);
            }
        }
    }
